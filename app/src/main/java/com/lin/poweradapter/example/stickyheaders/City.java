package com.lin.poweradapter.example.stickyheaders;

public class City {
    public String name;
    public String pys;
    public int type;

    @Override
    public String toString() {
        return "City{" +
                "name='" + name + '\'' +
                ", pys='" + pys + '\'' +
                ", type=" + type +
                '}';
    }
}
